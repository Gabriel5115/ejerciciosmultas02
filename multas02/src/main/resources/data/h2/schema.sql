CREATE TABLE AGENTES (


	CODIGO			BIGINT			NOT NULL,
	NOMBRE			VARCHAR(200)	,
	APELLIDO1		VARCHAR(200)	,
	APELLIDO2		VARCHAR(200)	,
	
	PRIMARY KEY(CODIGO)

);


CREATE TABLE VEHICULOS (


	MATRICULA		VARCHAR(200)			NOT NULL,
	MARCA			VARCHAR(200)	,
	MODELO			VARCHAR(200)	,
	COLOR			VARCHAR(200)	,
	
	PRIMARY KEY(MATRICULA)

);

CREATE TABLE MULTAS (


	CODIGO						BIGINT				NOT NULL,
	CODIGO_AGENTE				BIGINT				NOT NULL,
	MATRICULA_VEHICULO			VARCHAR(200)		NOT NULL,
	FECHA_HORA					TIMESTAMP			,
	LONGITUD					BIGINT				,
	LATITUD						BIGINT				,
	IMPORTE						DOUBLE				,
	OBSERVACIONES				VARCHAR(1000)		,
	INFRACCION					VARCHAR(10)			,
	
	
	
	PRIMARY KEY(CODIGO),
	FOREIGN KEY(CODIGO_AGENTE) REFERENCES AGENTES(CODIGO),
	FOREIGN KEY(MATRICULA_VEHICULO) REFERENCES VEHICULOS(MATRICULA)

);




